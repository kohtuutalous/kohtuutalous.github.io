<h1>Kohtuutalous</h1>
<p>Kohtuutalous (eng. degrowth) tähtää ekologisesti kestävään, hyvinvoivaan ja demokraattiseen yhteiskuntaan, jossa talouden järjestelmät eivät perustu jatkuvan kasvun tavoitteluun. Kohtuuden keskiössä on sekä talousjärjestelmän että kulttuurin muutos. Ekologisesti kestävässä yhteiskunnassa nostetaan arvoon asioita, joita nykyisessä järjestelmässä ei huomioida riittävästi, kuten hoiva, ympäristönsuojelu, yhteisöllisyys ja vapaa-aika.</p>
